
function ocultaMenu() {
    if (document.getElementById("menuPrincipal").style.display == "none") {
        document.getElementById("menuPrincipal").style.display = "block";
    } else {
        document.getElementById("menuPrincipal").style.display = "none";
    }
    if (document.getElementById("rodape").style.display == "none") {
        document.getElementById("rodape").style.display = "block";
    } else {
        document.getElementById("rodape").style.display = "none";
    }
}
